x = int(input('정수를 입력하세요: '))
y = int(input('정수를 입력하세됴: '))

print(f'{x} + {y} = {x + y}')
print(f'{x} - {y} = {x - y}')
print(f'{x} * {y} = {x * y}')
print(f'{x} / {y} = {x / y}')
print(f'{x} // {y} = {x // y}')
print(f'{x} % {y} = {x % y}')
print(f'{x} ** {y} = {x ** y}')